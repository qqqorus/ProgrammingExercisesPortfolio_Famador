# Primitive Quiz

# write a program that asks the user "What is the capital of France?" and waits for their response.
# france
user_answer_france = str(input("What is the capital of France? "))
correct_answer_france = "Paris"

# if the user's answer is correct (i.e., "Paris"), the program should print a message saying the answer is correct.
if user_answer_france.lower() == correct_answer_france.lower(): # .lower() ignores capitalization
    print("You got it correct!")
else:
    print("You are wrong. The correct answer is Paris.")
